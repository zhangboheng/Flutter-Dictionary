//Overview

/*

Dart is an object-oriented language with C-style syntax which can optionally 
trans compile into JavaScript. It supports a varied range of programming aids 
like interfaces, classes, collections, generics, and optional typing.

Dart can be extensively used to create single-page applications. Single-page 
applications apply only to websites and web applications. Single-page 
applications enable navigation between different screens of the website without 
loading a different webpage in the browser. A classic example is GMail ─ when 
you click on a message in your inbox, browser stays on the same webpage, but 
JavaScript code hides the inbox and brings the message body on screen.

Google has released a special build of Chromium – the Dart VM. Using 
Dartium means you don’t have to compile your code to JavaScript until you’re 
ready to test on other browsers.

You may test your scripts online by using the online editor at 
https://dartpad.dartlang.org/. The Dart Editor executes the script and displays 
both HTML as well as console output. The online editor is shipped 
with a set of preset code samples.

*/

